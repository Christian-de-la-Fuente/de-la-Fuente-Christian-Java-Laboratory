package interfaces;

public interface IMisurePoligono {
    /**
     * Metodo che presa un'istanza di un oggetto derivato da poligono, ne calcola l'area
     * @return il valore calcolato dell'area
     */
    public double calcolaPerimetro();
    /**
     * Metodo che presa l'istanza di un oggetto derivato da poligono, ne calcola il perimetro
     * @return il valore calcolato del perimetro
     */
    public double calcolaArea();
}
